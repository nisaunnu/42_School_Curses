void	ft_strncpy(char *dst, const char *src, int n)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dst[j])
		j++;
	while (src[i] && i < n)
		dst[j++] = src[i++];
	dst[j] = 0;
}
